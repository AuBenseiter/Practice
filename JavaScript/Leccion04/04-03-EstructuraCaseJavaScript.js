let numero = 3;
let numeroTexto = "valo desconocido";
switch (numero) {
  case 1:
    numeroTexto = "numero uno";
    break;
  case 2:
    numeroTexto = "numero dos";
    break;
  case 3:
    numeroTexto = "numero tres";
    break;
  case 4:
    numeroTexto = "numero cuatro";
    break;
  default:
    numeroTexto = 'No existe en este case'
    break;
}

console.log(numeroTexto);


